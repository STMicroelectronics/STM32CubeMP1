/**
  * @brief  Function implementing the THREAD1 thread.
  * @param  argument: Not used 
  * @retval None
  */
/* USER CODE END Header_LED_Thread1 */
static void LED_Thread1(void * argument)
{

  /* USER CODE BEGIN 5 */
  uint32_t count = 0;
  (void) argument;
  /* Infinite loop */
  for (;;)
  {
    count = osKernelGetTickCount() + 5000;

    /* Turn on LED4 */
    BSP_LED_On(LED4);

    while (count > osKernelGetTickCount())
    {
      /* Toggle LED4 every 250ms*/
      osDelay(250);
      BSP_LED_Toggle(LED4);
    }

    /* Turn off LED4 */
    BSP_LED_Off(LED4);

    /* Resume Thread 2 */
    osThreadResume(THREAD2Handle);
    /* Suspend Thread 1 : current thread */
    osThreadSuspend(THREAD1Handle);
  }
  /* USER CODE END 5 */ 
}

