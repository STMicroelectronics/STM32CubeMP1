/**
  * @brief  Function implementing the THREAD1 thread.
  * @param  argument: Not used 
  * @retval None
  */
/* USER CODE END Header_LED_Thread1 */
void LED_Thread1(void const * argument)
{

  /* USER CODE BEGIN 5 */
  uint32_t count = 0;
  (void) argument;
  /* Infinite loop */
  for (;;)
  {
    count = osKernelSysTick() + 5000;

    /* Turn on LED7 */
    BSP_LED_On(LED7);

    while (count > osKernelSysTick())
    {
      /* Toggle LED7 every 250ms*/
      osDelay(250);
      BSP_LED_Toggle(LED7);
    }

    /* Turn off LED7 */
    BSP_LED_Off(LED7);

    /* Resume Thread 2 */
    OsStatus = osThreadResume(THREAD2Handle);
    /* Suspend Thread 1 : current thread */
    OsStatus = osThreadSuspend(THREAD1Handle);
  }
  /* USER CODE END 5 */ 
}

